package com.techaxis.lab1;

public class Lab1 {
    public static void main(String[] args){
        //create variables
        int a=4;
        int var;

        //assign value using =
        var = a;
        System.out.println("var using =: " +var);

        //assign value using =+
        var += a;
        System.out.println("var using +=: " +var);

        //assign value using =+
        var *= a;
        System.out.println("var using *=: "+var);
    }
}

